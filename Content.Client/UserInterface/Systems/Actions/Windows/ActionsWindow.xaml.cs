using Content.Client.RussStation.UI;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.UserInterface.Systems.Actions.Windows;

[GenerateTypedNameReferences]
public sealed partial class ActionsWindow : DefaultWindow
{
    public const string StyleClassActionSearchBox = "actionSearchBox";

    //HONK - Inline collapsible filter panel instead of the upstream dropdown.
    public HonkFilterPanel<Filters> FilterButton { get; private set; }

    /// <summary>
    /// Whether the displayed actions or search filter needs updating.
    /// </summary>
    public bool UpdateNeeded;

    public ActionsWindow()
    {
        RobustXamlLoader.Load(this);

        //HONK - header sits inline in the search row; body drops under it when expanded
        FilterButton = new HonkFilterPanel<Filters>
        {
            Label = Loc.GetString("ui-actionmenu-filter-button")
        };
        SearchContainer.AddChild(FilterButton.Header);
        FilterContainer.AddChild(FilterButton.Body);

        foreach (var filter in Enum.GetValues<Filters>())
        {
            FilterButton.AddItem(Loc.GetString($"ui-actionmenu-{filter.ToString().ToLower()}"), filter);
        }
    }

    public enum Filters
    {
        Enabled,
        Item,
        Innate,
        Instant,
        Targeted,
        //HONK START - emote-as-action filter (#579)
        Emote,
        //HONK END
    }
}
